echo "A diferença de $a e $b é: $diferenca"
echo "O produto de $a e $b é: $produto"
echo "O quociente de $a e $b é: $quociente"
echo "O resto da divisão de $a por $b é: $resto"
echo "Cálculos concluídos."
echo "Fim do script."
